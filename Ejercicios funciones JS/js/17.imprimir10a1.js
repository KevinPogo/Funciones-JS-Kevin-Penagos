// Función para imprimir números del 10 al 1 en orden decreciente
function imprimirNumeros() {
    // Bucle `for` para iterar desde 10 hasta 1 (en orden decreciente)
    for (let i = 10; i >= 1; i--) {
      // Imprime el valor actual de `i` en la consola
      console.log(i);
    }
  }
  
  // Ejemplo de uso
  imprimirNumeros();
  