// Esta función suma dos números y devuelve la suma
function sumaNumeros(num1, num2) {
    // Suma los dos números pasados como argumentos
    const suma = num1 + num2;
    // Devuelve la suma calculada
    return suma;
  }
  
  // Llama a la función sumaNumeros con los argumentos 3 y 9
  console.log(sumaNumeros(3, 9));
  