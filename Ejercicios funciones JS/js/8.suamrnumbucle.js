// Función para sumar los números del 1 al 10
function sumarNumeros() {
    // Variable para almacenar la suma
    let suma = 0;
  
    // Bucle for para recorrer del 1 al 10
    for (let i = 1; i <= 10; i++) {
      // Agregar el número actual a la suma
      suma += i;
    }
  
    // Mostrar el resultado de la suma
    console.log("La suma de los números del 1 al 10 es:", suma);
  }
  
  // Llamar a la función para realizar la suma
  sumarNumeros();